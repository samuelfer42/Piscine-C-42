char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	chain;

	a = 0;
	b = 0;
	while (str[a] != '\0')
	{
		a++;
	}
	a--;
	while (b < a)
	{
		chain = str[a];
		str[a] = str[b];
		str[b] = chain;
		a--;
		b++;
	}
	return (str);
}
